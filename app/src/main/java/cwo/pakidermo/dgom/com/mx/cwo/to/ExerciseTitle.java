public class ExerciseTitle extends ExerciseDataType {

    private int total;
    private int office;
    private int challenges;
    private int tactfit;
    private int streching;

    public int getStreching() {
        return streching;
    }

    public void setStreching(int streching) {
        this.streching = streching;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getOffice() {
        return office;
    }

    public void setOffice(int office) {
        this.office = office;
    }

    public int getChallenges() {
        return challenges;
    }

    public void setChallenges(int challenges) {
        this.challenges = challenges;
    }

    public int getTactfit() {
        return tactfit;
    }

    public void setTactfit(int tactfit) {
        this.tactfit = tactfit;
    }
}
